


public class Items
{
    String name;
    String description;

    public Items(String name, String description)
    {
        this.name = name;
        this.description = description;

    }

    public String getName()
    {
        return name;
    }

    public String getItemInfo(String description)
    {
        return description;
    }

    public String getShortDescription()
    {
        return description;
    }





}
